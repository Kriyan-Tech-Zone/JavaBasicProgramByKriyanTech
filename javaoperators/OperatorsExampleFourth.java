package javaoperators;

public class OperatorsExampleFourth {
    public static void main(String[] args) {

        int a = 52;
        int b = 8;

        System.out.println(a & b);

        System.out.println(a | b);

        System.out.println(a ^ b);

        System.out.println(~a);

        System.out.println(~b);


    }
}
