public class JavaArraySize {
    public static void main(String[] args) {
  
         int [] numbers = new int [] {58,26,78,45,98,3,6};       
         System.out.println("Size of int array is:" + numbers.length);
  
         String [] names = new String [] {"Neha", "Priya", "Usha"};
         System.out.println("Size of String array is:" + names.length);
    }
}
